public class EmptySpace {
    public int volume = 0;
    public int xPos = 0;
    public int yPos = 0;
    public int zPos = 0;

    public EmptySpace(int volume, int xPos, int yPos, int zPos) {
        this.volume = volume;
        this.xPos = xPos;
        this.yPos = yPos;
        this.zPos = zPos;
    }
}
